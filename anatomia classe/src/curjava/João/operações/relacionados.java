package curjava.João.operações;

public class relacionados {
    public static void main(String[] args) {
        int num1 = 1;
        int num2 = 2;

        boolean simNao = num1 == num2;

        System.out.println("NÚMERO 1 É IGUAL AO NÚMERO 2?" + simNao);

        simNao = num1 != num2;

        System.out.println("NÚMERO 1 É DIFERENTE DO NÚMERO 2?" + simNao);

        simNao = num1 > num2;

        System.out.println("NÚMERO 1 É MAIOR QUE NÚMERO 2?" + simNao);

        
    }
    
}
