package curjava.João.operações;

public class condicoe {
    public static void main(String[] args) {
        boolean cond1 = true;
        boolean cond2 = false;

        if(cond1 && cond2){
            System.out.print("as duas condições são verdadeiras");
        }

        if(cond1 || cond2){
            System.out.print("uma das duas condições são verdadeiras");
        }
    }
    
}
