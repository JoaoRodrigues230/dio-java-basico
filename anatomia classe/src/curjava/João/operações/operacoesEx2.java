package curjava.João.operações;

public class operacoesEx2 {
    public static void main(String[] args) {
       int num = 5;

        num = - num;

        System.out.println(num);

        num = num * -1; //tornando ele positivo novamente

        System.out.println(num);
        
        num++; //= num + 1

        boolean variavel = true;

        variavel = !variavel;//inverssão de true para false

        System.out.println(variavel);
    }
    
}
